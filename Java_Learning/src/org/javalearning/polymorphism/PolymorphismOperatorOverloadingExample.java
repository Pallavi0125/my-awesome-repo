package org.javalearning.polymorphism;
 
/*Operator Overloading: 
*  One operator with many functionality called as operator overloading.
*  Java doesn't support operator overloading concepts
*  But only one operator i.e "Plus operator" supports operator overloading concepts 
*/

public class PolymorphismOperatorOverloadingExample {
	public static void main(String[] args) {
		// Operator Overloading
		System.out.println(10+20);
		System.out.println("Pallavi" +" Anand");
		System.out.println("Pallavi" + 10 + "Anand" + 20 + 30);

	}

}
